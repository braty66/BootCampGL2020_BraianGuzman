package com.globallogic.Interfaces;

public class Animal {
	
	private String estructuraInterna;//vertebrados o invertebrados
	private String formaDeReproduccion;// Pueden ser ovíparos, vivíparos y ovovivíparos.
	private String tipoDeAlimentacion;//Entre ellos tenemos herbívoros, carnívoros y omnívoros.
	private String habitat;//terrestres o acuáticos.
	
	
	public Animal(String estructuraInterna, String formaDeReproduccion, String tipoDeAlimentacion, String habitat) {
		super();
		this.estructuraInterna = estructuraInterna;
		this.formaDeReproduccion = formaDeReproduccion;
		this.tipoDeAlimentacion = tipoDeAlimentacion;
		this.habitat = habitat;
	}
	
	
	
	
	

}
